export function getPoiGalleryItems(poi) {
  if (!poi) return [];
  const pack = BY_POI[poi.id];
  if (pack?.gallery?.length) return pack.gallery;
  return DEFAULT_GALLERY(poi.title || "地点");
}

export function getPoiNoteFeedItems(poi) {
  if (!poi) return [];
  const pack = BY_POI[poi.id];
  if (pack?.notes?.length) return pack.notes;
  return DEFAULT_NOTES(poi.title || "这里");
}
